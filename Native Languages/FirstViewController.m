- (MKOverlayView *)mapView:(MKMapView *)mapView viewForOverlay:(id<MKOverlay>)overlay
{
    if([[overlay title] isEqualToString:@"Inupiaq"])
    {
        MKPolygonView *view = [[MKPolygonView alloc] initWithPolygon:(MKPolygon*)overlay];
        view.strokeColor = [[UIColor blackColor] colorWithAlphaComponent:0.2];
        view.fillColor = [[UIColor redColor] colorWithAlphaComponent:1];
        view.lineWidth = 5.0;
        return view;
    }
    
    else if([[overlay title] isEqualToString:@"Yupik"])
    {
        MKPolygonView *view = [[MKPolygonView alloc] initWithPolygon:(MKPolygon*)overlay];
        view.strokeColor = [[UIColor blackColor] colorWithAlphaComponent:0.2];
        view.fillColor = [[UIColor blueColor] colorWithAlphaComponent:1];
        view.lineWidth = 5.0;
        return view;
    }

    
    else if([overlay isKindOfClass:[MKPolygon class]]){
        MKPolygonView *view = [[MKPolygonView alloc] initWithPolygon:(MKPolygon*)overlay];
        view.strokeColor = [[UIColor blackColor] colorWithAlphaComponent:0.2];
        view.fillColor = [[UIColor grayColor] colorWithAlphaComponent:1];
        view.lineWidth = 5.0;
        return view;
    }
    return nil;
}


- (MKAnnotationView *)mapView:(MKMapView *)mapView viewForAnnotation:(id<MKAnnotation>)annotation
{
    if([[annotation title] isEqualToString:@"Inupiaq"])
    {
        NSLog(@"Inside view for annot");
        MKPinAnnotationView *enhanceView = [[MKPinAnnotationView alloc] initWithAnnotation:annotation reuseIdentifier:nil];
        UIButton *infoButton = [UIButton buttonWithType:UIButtonTypeDetailDisclosure];
        enhanceView.pinColor = MKPinAnnotationColorGreen;
        enhanceView.rightCalloutAccessoryView = infoButton;
        enhanceView.canShowCallout = YES;
        return enhanceView;
        
    }
    
    if([[annotation title] isEqualToString:@"CentralYupik"])
    {
        NSLog(@"Inside view for annot");
        MKPinAnnotationView *enhanceView = [[MKPinAnnotationView alloc] initWithAnnotation:annotation reuseIdentifier:nil];
        UIButton *infoButton = [UIButton buttonWithType:UIButtonTypeDetailDisclosure];
        enhanceView.pinColor = MKPinAnnotationColorGreen;
        enhanceView.rightCalloutAccessoryView = infoButton;
        enhanceView.canShowCallout = YES;
        return enhanceView;
        
    }
    
    else{
        NSLog(@"For Title: %@", [annotation title]);
    }
    
    return nil;
}


